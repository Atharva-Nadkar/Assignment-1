#include<algorithm>
#include<stdio.h>
#include<iostream>
#include<string>
#include<stdio.h>
#include<math.h>
#include<sstream>

using namespace std;
 
int main()
{
    cin.tie(NULL);
    ios_base::sync_with_stdio(false);
    
    char c;
    int i;
    float f;
    double d;
    
    cout <<"Size of char: "<<sizeof(c)<<" byte"<<endl;
    cout <<"Size of int: "<<sizeof(i)<<" bytes"<<endl;
    cout <<"Size of float: "<<sizeof(f)<<" bytes"<<endl;
    cout <<"Size of double: "<<sizeof(d)<<" bytes"<<endl;
    
    return 0;
}
